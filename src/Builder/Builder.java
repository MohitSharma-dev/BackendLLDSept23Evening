package Builder;

public class Builder {
    String name;
    int age;
    int gradYear;
    String batch;

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setGradYear(int gradYear) {
        this.gradYear = gradYear;
    }

    public void setBatch(String batch) {
        this.batch = batch;
    }
}
